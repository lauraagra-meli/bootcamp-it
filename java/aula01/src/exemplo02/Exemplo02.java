package exemplo02;
// Condicionais - IF

public class Exemplo02 {
    public static void main(String[] args) {
        int number = 0;

        if(number > 0) {
            System.out.println("number > 0");
        } else if(number == 0) {
            System.out.println("number = 0");
        } else {
            System.out.println("number < 0");
        }

        System.out.println("Fim");
    }
}
